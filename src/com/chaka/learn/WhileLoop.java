package com.chaka.learn;

public class WhileLoop {

    public static void main(String args[]) {

        int counter = 0;

        while (counter < 10) {

            System.out.println(counter);
            counter++;
        }

        int count = 0;

        while (count < 100 ) {

            System.out.println(count);
            count++;
        }
    }
}
